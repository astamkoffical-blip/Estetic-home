const Delivery = {
  storageKey: 'ed_delivery_data',
  loadSaved() {
    try { return JSON.parse(localStorage.getItem(this.storageKey) || '{}'); }
    catch(e){ return {}; }
  },
  saveSaved(data) {
    localStorage.setItem(this.storageKey, JSON.stringify(data));
  },
  renderMap() {
    const city = ($('delCity')||{}).value || '';
    const addr = ($('delAddr')||{}).value || '';
    const frame = $('delMap');
    if (!frame) return;
    const q = encodeURIComponent((city + ' ' + addr).trim() || city || 'Казань');
    frame.src = 'https://www.google.com/maps?q=' + q + '&output=embed';
  },
  // Получить стоимость доставки
  calc(cityName, totalWeight) {
    const row = DELIVERY_RATES.find(r =>
      r[0].toLowerCase() === cityName.toLowerCase()
    );
    if (!row) return null;

    let idx;
    if (totalWeight <= 100) idx = 1;
    else if (totalWeight <= 200) idx = 2;
    else if (totalWeight <= 300) idx = 3;
    else idx = 4; // 300+ кг = максимальная ставка

    return {
      city: row[0],
      price: row[idx] || row[4],
      weight: totalWeight,
      range: totalWeight <= 100 ? 'до 100 кг'
        : totalWeight <= 200 ? '100–200 кг'
        : totalWeight <= 300 ? '200–300 кг'
        : 'свыше 300 кг'
    };
  },

  // Общий вес корзины
  cartWeight() {
    let w = 0;
    cart.forEach(item => {
      const prod = PRODUCTS.find(p => p.name === item.n);
      if (prod) {
        const kg = parseFloat(prod.wt) || 20;
        w += kg * item.q;
      }
    });
    return w;
  },

  // Список городов для автокомплита
  cities() {
    return DELIVERY_RATES.map(r => r[0]);
  },

  // Открыть модалку доставки
  open() {
    Modal.open('deliveryModal');
    this.render();
  },

  render() {
    const w = this.cartWeight();
    const body = $('deliveryBody');
    const saved = this.loadSaved();
    const city = saved.city || selectedCity || '';
    const name = saved.name || '';
    const addr = saved.addr || '';
    const phone = saved.phone || '';
    const time = saved.time || 'Любое время';
    body.innerHTML = `
      <h2>Рассчитать доставку</h2>
      <p class="sub-p">Укажите город и данные доставки — стоимость
        будет рассчитана автоматически по весу заказа</p>

      <div class="del-weight">
        <span>Вес заказа:</span>
        <strong>${w.toFixed(1)} кг</strong>
      </div>

      <div class="del-search-wrap">
        <label class="fg-label">Город доставки</label>
        <input class="inp" id="delCity" placeholder="Начните вводить город..."
          value="${city}" oninput="Delivery.suggest()"
          onfocus="Delivery.suggest()" autocomplete="off">
        <div class="del-suggest" id="delSuggest"></div>
      </div>

      <div class="del-fields" id="delFields">
        <label class="fg-label">Ваше имя</label>
        <input class="inp" id="delName" placeholder="Как к вам обращаться?" value="${name}">

        <label class="fg-label">Адрес доставки</label>
        <input class="inp" id="delAddr" placeholder="Улица, дом, квартира, этаж" value="${addr}">

        <div style="display:grid;grid-template-columns:1fr 1fr;gap:10px">
          <div>
            <label class="fg-label">Телефон</label>
            <input class="inp" id="delPhone" placeholder="+7 (___) ___-__-__" type="tel" value="${phone}">
          </div>
          <div>
            <label class="fg-label">Удобное время</label>
            <select class="inp" id="delTime" style="margin-bottom:14px">
              <option ${time==='Любое время'?'selected':''}>Любое время</option>
              <option ${time==='09:00 – 12:00'?'selected':''}>09:00 – 12:00</option>
              <option ${time==='12:00 – 15:00'?'selected':''}>12:00 – 15:00</option>
              <option ${time==='15:00 – 18:00'?'selected':''}>15:00 – 18:00</option>
              <option ${time==='18:00 – 21:00'?'selected':''}>18:00 – 21:00</option>
            </select>
          </div>
        </div>

        <div class="del-map-card">
          <div class="del-map-icon">📍</div>
          <div>
            <strong>Адрес на карте</strong>
            <span>Проверьте район доставки и уточните подъезд к дому</span>
          </div>
        </div>
        <div class="del-map-embed">
          <iframe id="delMap"
            title="Карта доставки"
            loading="lazy"
            referrerpolicy="no-referrer-when-downgrade"
            src="about:blank"></iframe>
        </div>
      </div>

      <div class="del-result" id="delResult"></div>
    `;
    if (city) {
      const res = this.calc(city, w);
      if (res) {
        $('delResult').innerHTML = this.resultHtml(res);
      }
    }

    if (window.PhoneMask) PhoneMask.bindAll($('deliveryModal'));

    ['delCity','delName','delAddr','delPhone','delTime'].forEach(id => {
      const el = $(id);
      if (!el) return;
      el.addEventListener('change', () => {
        this.saveSaved({
          city: $('delCity')?.value || '',
          name: $('delName')?.value || '',
          addr: $('delAddr')?.value || '',
          phone: $('delPhone')?.value || '',
          time: $('delTime')?.value || 'Любое время'
        });
        this.renderMap();
      });
    });
    this.renderMap();
  },

  resultHtml(res) {
    return `
      <div class="del-card">
        <div class="del-card-row"><span>Город:</span><strong>${res.city}</strong></div>
        <div class="del-card-row"><span>Вес заказа:</span><strong>${res.weight.toFixed(1)} кг (${res.range})</strong></div>
        <div class="del-card-row del-total"><span>Стоимость доставки:</span><strong>${fmt(res.price)} ₽</strong></div>
      </div>
      <button class="btn-p w100" style="margin-top:16px" onclick="Delivery.confirm()">Подтвердить доставку</button>`;
  },
  suggest() {
    const q = $('delCity').value.trim().toLowerCase();
    const wrap = $('delSuggest');
    if (q.length < 1) { wrap.innerHTML = ''; wrap.style.display = 'none'; return; }

    const found = DELIVERY_RATES.filter(r =>
      r[0].toLowerCase().includes(q)
    ).slice(0, 8);

    if (!found.length) {
      wrap.innerHTML = '<div class="del-sg-empty">Город не найден</div>';
      wrap.style.display = 'block';
      return;
    }

    const wt = this.cartWeight();
    wrap.innerHTML = found.map(r => {
      const res = this.calc(r[0], wt);
      return `<div class="del-sg-item" onclick="Delivery.pick('${r[0]}')">
        <span>${r[0]}</span>
        <span class="del-sg-price">${res ? fmt(res.price) + ' ₽' : ''}</span>
      </div>`;
    }).join('');
    wrap.style.display = 'block';
  },

  pick(city) {
    $('delCity').value = city;
    $('delSuggest').style.display = 'none';
    const s = this.loadSaved();
    this.saveSaved({...s, city});

    const w = this.cartWeight();
    const res = this.calc(city, w);

    if (!res) {
      $('delResult').innerHTML = '<div class="del-err">Город не найден в списке</div>';
      return;
    }

    $('delResult').innerHTML = this.resultHtml(res);
  },

  confirm() {
    const city = $('delCity').value;
    const name = $('delName')?.value || '';
    const addr = $('delAddr')?.value || '';
    const phone = $('delPhone')?.value || '';

    if (!city) { alert('Пожалуйста, выберите город'); return; }
    if (!name) { alert('Пожалуйста, укажите имя'); return; }
    if (!addr) { alert('Пожалуйста, заполните адрес'); return; }
    if (!phone) { alert('Пожалуйста, заполните телефон'); return; }

    this.saveSaved({
      city, name, addr, phone,
      time: ($('delTime')||{}).value || 'Любое время'
    });

    $('delResult').innerHTML = `
      <div class="del-ok">
        <svg class="ico ico-48" style="color:var(--g600)">
          <use href="#i-check"/></svg>
        <h3>Заявка отправлена!</h3>
        <p>${name}, наш менеджер свяжется с вами для подтверждения
          доставки в ${city}</p>
      </div>
    `;
  }
};
